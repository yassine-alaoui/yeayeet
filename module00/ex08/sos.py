import sys
morse = "/ --.- --.- --.- --.- --.- --.- --.- --.- --.- --.- --.- --.- --.- --.- --.- ----- .---- ..--- ...-- ....- ..... -.... --... ---.. ----. --.- --.- --.- --.- --.- --.- --.- .- -... -.-. -.. . ..-. --. .... .. .--- -.- .-.. -- -. --- .--. --.- .-. ... - ..- ...- .-- -..- -.-- --.. --.- --.- --.- --.- --.- --.- .- -... -.-. -.. . ..-. --. .... .. .--- -.- .-.. -- -. --- .--. --.- .-. ... - ..- ...- .-- -..- -.-- --.. --.- --.- --.- --.-".split(" ")
if len(sys.argv) < 2:
	print("ERROR")
	exit(1)

encode = ""
for arg in sys.argv[1:]:
	if not arg.replace(' ', '').isalnum():
		print("ERROR")
		exit(1)
	encode += " ".join([morse[ord(el) - 32] for el in arg])
	encode += " / "

print(encode[:-2])